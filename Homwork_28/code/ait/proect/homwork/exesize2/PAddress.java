package ait.proect.homwork.exesize2;

public class PAddress {
//        Задача 2 (*).
//        Программа получает на вход IP-адрес в одну строку: например, 192.168.23.1
//        (четыре числа, разделённые точками).
//        Каждое число должно быть в диапазоне от 0 до 255.
//        Программа должна вывести эти четыре числа по отдельности.

    public boolean idCheck(String id) {
        id = id.trim(); // Убираем лишние пробелы в начале и конце строки

        // Разбиваем строку на массив строк, используя точку как разделитель
        String[] idArrNum = id.split("\\.");

        // Проверяем, что получили ровно 4 элемента после разбиения
        if (idArrNum.length != 4) {
            return false; // Возвращаем false, если количество элементов не равно 4
        }

        // Проходим по каждому элементу в массиве
        for (int i = 0; i < idArrNum.length; i++) {
            int idNum = Integer.parseInt(idArrNum[i]); // Преобразуем элемент в число

            // Выводим число на экран и определяем, нужно ли поставить запятую или точку с запятой в конце
            System.out.print(idNum + (i < idArrNum.length - 1 ? "," : ";"));

            // Проверяем, что число находится в допустимом диапазоне
            if (idNum <= 0 || idNum >= 255) {
                return false; // Возвращаем false, если число вне допустимого диапазона
            }
        }

        return true; // Если все проверки пройдены успешно, возвращаем true
    }

    //    ****************************************************************************************


}
